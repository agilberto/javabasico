package cursoJava.baisco;

/*
 * Classe Demonstrativa de variaveis e constantes
 */

public class VariaveisEConstantes {

	public static void main(String[] args) {
		
		// Criação de constantes
		final double ACELARACAO_GRAVIDADE_TERRA = 9.80065;
		
		String nome = "José";
		
		int idade = 48;
		
		double peso = 73.4, altura = 1.73;
		
		char sexo = 'm',cnh = 'a';
		
		boolean doadorOrgaos = true;
		
		
		System.out.println("Nome: "+nome);
		System.out.println("Idade: "+idade);
		System.out.println("Peso: "+peso);
		System.out.println("Altura: "+altura);
		System.out.println("Sexo: "+sexo);
		System.out.println("Cnh: "+cnh);
		System.out.println("Doador de Orgão: "+doadorOrgaos);
		System.out.println("Gravidade: "+ACELARACAO_GRAVIDADE_TERRA);
		
		
		 
	}

}
